
public class Item {
	
	private double peso;
	private String desc;
	
	

	public Item(String desc, double peso) {
		super();
		this.peso = peso;
		this.desc = desc;
	}

	public double getPeso() {
		return peso;
	}

	public void setPeso(double peso) {
		this.peso = peso;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
	
	
	

}
